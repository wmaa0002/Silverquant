def get_market_type(code: str) -> str:
    """判断市场类型"""
    if code.startswith('688'):
        return '科创板'
    elif code.startswith('300') or code.startswith('301'):
        return '创业板'
    elif code.startswith('8') or code.startswith('4'):
        return '北交所'
    elif code.startswith('6'):
        return '沪市主板'
    else:
        return '深市主板'


def fetch_stock_info_from_baostock(codes: list) -> dict:
    """从baostock获取股票信息"""
    lg = bs.login()
    if lg.error_code != '0':
        print(f"baostock登录失败: {lg.error_msg}")
        return {}
    
    stock_info = {}
    try:
        rs = bs.query_stock_basic()
        while rs.next():
            row = rs.get_row_data()
            code_full = row[0]  # 格式: sh.600000
            code = code_full.replace('sh.', '').replace('sz.', '')
            
            if code in codes:
                stock_info[code] = {
                    'name': row[1],           # 股票名称
                    'listing_date': row[2],   # 上市日期
                    'delisted_date': row[3] if row[3] else None,  # 退市日期
                    'type': row[4],           # 股票类型
                }
    finally:
        bs.logout()
    
    return stock_info


def fetch_stock_info_from_akshare(codes: list) -> dict:
    """从akshare获取股票信息"""
    stock_info = {}
    try:
        # 获取实时行情数据
        df = ak.stock_zh_a_spot_em()
        
        for code in codes:
            row = df[df['代码'] == code]
            if not row.empty:
                stock_info[code] = {
                    'name': row.iloc[0]['名称'],
                    'price': row.iloc[0]['最新价'],
                    'change_pct': row.iloc[0]['涨跌幅'],
                    'is_st': 'ST' in str(row.iloc[0]['名称']),
                }
    except Exception as e:
        print(f"akshare获取失败: {e}")
    
    return stock_info


def generate_blacklist_report(codes: list, output_path: str = 'blacklist.md'):
    """生成黑名单报告"""
    print(f"开始获取 {len(codes)} 只股票的信息...")
    
    # 从baostock获取基本信息
    print("从baostock获取基本信息...")
    bs_info = fetch_stock_info_from_baostock(codes)
    print(f"  获取到 {len(bs_info)} 只股票的基本信息")
    
    # 从akshare获取实时信息
    print("从akshare获取实时信息...")
    ak_info = fetch_stock_info_from_akshare(codes)
    print(f"  获取到 {len(ak_info)} 只股票的实时信息")
    
    # 合并信息
    stocks = []
    for code in codes:
        bs_data = bs_info.get(code, {})
        ak_data = ak_info.get(code, {})
        
        stock = {
            'code': code,
            'name': bs_data.get('name') or ak_data.get('name', '未知'),
            'market': get_market_type(code),
            'listing_date': bs_data.get('listing_date'),
            'delisted_date': bs_data.get('delisted_date'),
            'is_delisted': True if bs_data.get('delisted_date') else False,
            'price': ak_data.get('price'),
            'change_pct': ak_data.get('change_pct'),
            'is_st': ak_data.get('is_st', False),
        }
        stocks.append(stock)
    
    # 统计
    total = len(stocks)
    delisted = sum(1 for s in stocks if s['is_delisted'])
    st_count = sum(1 for s in stocks if s['is_st'])
    active = total - delisted
    
    # 按市场分类统计
    market_stats = {}
    for s in stocks:
        market = s['market']
        if market not in market_stats:
            market_stats[market] = {'total': 0, 'delisted': 0, 'st': 0}
        market_stats[market]['total'] += 1
        if s['is_delisted']:
            market_stats[market]['delisted'] += 1
        if s['is_st']:
            market_stats[market]['st'] += 1
    
    # 生成Markdown报告
    lines = []
    lines.append("# 股票黑名单报告\n")
    lines.append(f"生成时间: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
    lines.append(f"股票总数: {total}\n")
    lines.append(f"- 已退市: {delisted}\n")
    lines.append(f"- ST股票: {st_count}\n")
    lines.append(f"- 在交易: {active}\n")
    lines.append("\n## 按市场统计\n")
    lines.append("| 市场 | 总数 | 退市 | ST |")
    lines.append("|------|------|------|-----|")
    for market, stats in sorted(market_stats.items()):
        lines.append(f"| {market} | {stats['total']} | {stats['delisted']} | {stats['st']} |")
    
    lines.append("\n## 股票详情\n")
    lines.append("| 代码 | 名称 | 市场 | 上市日期 | 退市日期 | 状态 |")
    lines.append("|------|------|------|----------|----------|------|")
    
    # 按退市状态排序（退市的在前）
    stocks_sorted = sorted(stocks, key=lambda x: (not x['is_delisted'], x['code']))
    
    for s in stocks_sorted:
        status = []
        if s['is_delisted']:
            status.append('退市')
        if s['is_st']:
            status.append('ST')
        if not status:
            status.append('在交易')
        
        lines.append(f"| {s['code']} | {s['name']} | {s['market']} | {s['listing_date'] or '-'} | {s['delisted_date'] or '-'} | {', '.join(status)} |")
    
    # 写入文件
    content = '\n'.join(lines)
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print(f"\n报告已生成: {output_path}")
    print(f"  总数: {total}, 退市: {delisted}, ST: {st_count}, 在交易: {active}")


